package core_java;

public class UnaryOperators {

    public static void main(String[] args) {

        int a = 10, b = 0;
        b = ++a ; // post increment
        int c = b;
        System.out.println("value of a = " + a);
        System.out.println("value of b = " + b);
        System.out.println("value of c = " + c);


    }
}
